// Function to translate text
function translateText(text) {
  if (!text) return text;
  
  let translated = text;
  
  // Replace common terms
  Object.entries(translations).forEach(([english, swedish]) => {
    const regex = new RegExp(`\\b${english}\\b`, 'gi');
    translated = translated.replace(regex, swedish);
  });
  
  return translated;
}

// Function to translate a recipe
function translateRecipe(recipe) {
  return {
    ...recipe,
    name: translateText(recipe.name),
    description: translateText(recipe.description),
    ingredientsWithAmounts: recipe.ingredientsWithAmounts.map(item => translateText(item)),
    instructions: recipe.instructions.map(step => translateText(step)),
    difficulty: translations[recipe.difficulty] || recipe.difficulty,
    tags: recipe.tags ? recipe.tags.map(tag => translateText(tag)) : []
  };
}

// Translate all cuisines
Object.entries(enRecipes.cultural).forEach(([cuisine, recipes]) => {
  console.log(`ðŸ”„ Translating ${cuisine} recipes...`);
  
  const translatedRecipes = recipes.map(recipe => translateRecipe(recipe));
  svRecipes.cultural[cuisine] = translatedRecipes;
  
  console.log(`   âœ… Translated ${recipes.length} ${cuisine} recipes`);
});

