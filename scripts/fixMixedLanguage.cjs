// Function to translate mixed language text
function translateMixedText(text, targetLanguage) {
  if (!text || typeof text !== 'string') return text;
  
  const dictionary = targetLanguage === 'zh' ? englishToChinese : englishToSwedish;
  
  // Split text into words while preserving spaces and punctuation
  const words = text.split(/(\s+|[.,!?;:()"'`-])/);
  
  const translatedWords = words.map(word => {
    // Check if word is purely English (contains only English letters)
    if (/^[a-zA-Z]+$/.test(word)) {
      const lowerWord = word.toLowerCase();
      return dictionary[lowerWord] || word;
    }
    return word;
  });
  
  return translatedWords.join('');
}

// Function to process recipe file
function processRecipeFile(filePath, targetLanguage) {
  console.log(`Processing ${filePath} for ${targetLanguage}...`);
  
  try {
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    let changesCount = 0;
    
    // Process cultural recipes
    if (data.cultural) {
      Object.keys(data.cultural).forEach(cuisine => {
        if (Array.isArray(data.cultural[cuisine])) {
          data.cultural[cuisine].forEach(recipe => {
            // Translate recipe name
            if (recipe.name) {
              const newName = translateMixedText(recipe.name, targetLanguage);
              if (newName !== recipe.name) {
                recipe.name = newName;
                changesCount++;
              }
            }
            
            // Translate description
            if (recipe.description) {
              const newDescription = translateMixedText(recipe.description, targetLanguage);
              if (newDescription !== recipe.description) {
                recipe.description = newDescription;
                changesCount++;
              }
            }
            
            // Translate ingredients with amounts
            if (recipe.ingredientsWithAmounts && Array.isArray(recipe.ingredientsWithAmounts)) {
              recipe.ingredientsWithAmounts = recipe.ingredientsWithAmounts.map(ingredient => {
                const newIngredient = translateMixedText(ingredient, targetLanguage);
                if (newIngredient !== ingredient) {
                  changesCount++;
                }
                return newIngredient;
              });
            }
            
            // Translate instructions
            if (recipe.instructions && Array.isArray(recipe.instructions)) {
              recipe.instructions = recipe.instructions.map(instruction => {
                const newInstruction = translateMixedText(instruction, targetLanguage);
                if (newInstruction !== instruction) {
                  changesCount++;
                }
                return newInstruction;
              });
            }
          });
        }
      });
    }
    
    // Write back to file
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
    console.log(`‚úÖ ${filePath}: ${changesCount} changes made`);
    
    return changesCount;
  } catch (error) {
    console.error(`‚ùå Error processing ${filePath}:`, error.message);
    return 0;
  }
}

